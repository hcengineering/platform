function testGetDay (date: Date, timezone: string): void {
  const timestamp: Timestamp = getDay(date.getTime())
  const convertedDate: Date = new Date(timestamp)
  const originalLocaleDate: string = date.toLocaleDateString('en-US', { timeZone: 'Europe/London' })
  const convertedLocaleDate: string = convertedDate.toLocaleDateString('en-US', { timeZone: timezone })
  expect(convertedLocaleDate).toEqual(originalLocaleDate)
}

function testConvertToDay (date: Date, timezone: string): void {
  const convertedDate: Date = convertToDay(date)
  const originalLocaleDate: string = date.toLocaleDateString('en-US', { timeZone: 'Europe/London' })
  const convertedLocaleDate: string = convertedDate.toLocaleDateString('en-US', { timeZone: timezone })
  expect(convertedLocaleDate).toEqual(originalLocaleDate)
}

describe('time', () => {
  it.each(supportedTimezones)('dates are matched in `getDay` for locale %p', (timezone: string) => {
    testGetDay(new Date(Date.UTC(1995, 11, 3, 12, 0)), timezone)
    testGetDay(new Date(Date.UTC(2025, 0, 23, 55, 0)), timezone)
    testGetDay(new Date(Date.UTC(2020, 4, 12, 30, 50)), timezone)
    testGetDay(new Date(Date.UTC(2024, 5, 12, 30, 50)), timezone)
    testGetDay(new Date(Date.UTC(2024, 7, 0, 0, 0)), timezone)
  })

  it.each(supportedTimezones)('dates are matched in `convertToDay` for locale %p', (timezone: string) => {
    testConvertToDay(new Date(Date.UTC(1995, 11, 3, 12, 0)), timezone)
    testConvertToDay(new Date(Date.UTC(2025, 0, 23, 55, 0)), timezone)
    testConvertToDay(new Date(Date.UTC(2020, 4, 12, 30, 50)), timezone)
    testConvertToDay(new Date(Date.UTC(2024, 5, 12, 30, 50)), timezone)
    testConvertToDay(new Date(Date.UTC(2024, 7, 0, 0, 0)), timezone)
  })

  it.each([
    [0, 0, 0, 0],
    [7, 59, 59, 999],
    [12, 0, 0, 0],
    [23, 59, 59, 999]
  ])(
    'dates are matched for time [h: %p, m: %p, s: %p, ms: %p]',
    (hours: number, minutes: number, seconds: number, milliSeconds: number) => {
      const date: Date = new Date()
      const expectedDay: number = date.getDate()
      date.setHours(hours, minutes, seconds, milliSeconds)
      const convertedDate: Date = convertToDay(date)
      expect(convertedDate.getDate()).toEqual(expectedDay)
    }
  )
})
